 # Example of even or odd using if-else statements
number = int(input("Enter a number to check if it's even or odd: "))
if number % 2 == 0:
        print(f"{number} is even.")
else:
        print(f"{number} is odd.")
        
        
        
        
product = number * 2
print(f"The product of {number} and 2 is {product}.")